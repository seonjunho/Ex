package for1;

public class Ex5_1 {
    public static void main(String[] args) {
        for(int i = 0 ; i <= 5 ; i++)
            System.out.println(i); // i값을 출력한다.

        for(int i = 1 ; i <= 5 ; i++)
            System.out.print(i); // print() 를 쓰면 가로롤 출력된다.

        System.out.println();
    }
}
